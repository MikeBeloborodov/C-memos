// including standard c++ library
#include <iostream>

// using a namespace std in this program
using namespace std;

int main()
{
	// initialize a variable and give it a number
	int number;
	number = 2021;

	// initialize and give it a number at the same time
	int number2 = 2022;

	// print result on the screen
	cout << "This year is: ";
	cout << number;

	cout << "\nThe next year is: ";
	cout << number2;

	// returns 0 if program is fine, 1 if there is an error
	return 0;
}


